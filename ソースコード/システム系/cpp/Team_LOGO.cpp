//*****************************************************************************
// グローバル変数
//*****************************************************************************
static int	LOGOTex = 0;	// テクスチャ情報

static int	BackTex = 0;

int g_Time;
//=============================================================================
// 初期化処理
//=============================================================================
HRESULT InitLOGO(void)
{
	LOGOTex = LoadTexture("data/TEXTURE/LOGO.png");
	
	BackTex = LoadTexture("data/TEXTURE/Back.png");

	

	return S_OK;
}

//=============================================================================
// 終了処理
//=============================================================================
void UninitLOGO(void)
{

}

//=============================================================================
// 更新処理
//=============================================================================

void UpdateLOGO(void)
{

	g_Time++;
	if (g_Time > 50) {
		g_Time = 0;
		SceneTransition(SCENE_TITLE);
	}
}

//=============================================================================
// 描画処理
//=============================================================================
void DrawLOGO(void)
{
	// １枚のポリゴンの頂点とテクスチャ座標を設定
	DrawSpriteLeftTop(BackTex, 0.0f, 0.0f, SCREEN_WIDTH, SCREEN_HEIGHT, 0.0f, 0.0f, 1.0f, 1.0f);
	DrawSpriteLeftTop(LOGOTex, 350.0f, 50.0f, 600, 600, 0.0f, 0.0f, 1.0f, 1.0f);
	
}

